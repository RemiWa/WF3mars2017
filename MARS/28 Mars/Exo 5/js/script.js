//  Créer une variable de type numer avec la valeur 10

/*

    Opérateur d'affectation

*/

var myNumber = 10;
console.log(myNumber);

// Opérateur d'incrémentation: ++

myNumber++;
console.log(myNumber);

//  Opérateur de décrémentation

myNumber--;
console.log(myNumber);

//  Opérateur d'addition / Concaténation: +
var myAdd =7 + 5;
console.log(myAdd);

var myConc = 'Salut ' + 'mon ' + 'pote';
console.log(myConc);


// Opérateur de soustraction: -
var mySous = 7 - 5;
console.log(mySous);

// opérateur de multiplication
var myMulti = 7 * 5;
console.log(myMulti);

// Opérateur de division

var myDivide = 57 / 7;
console.log(myDivide);

// Opérateur modulo : %
var myModulo = 12 % 5;
console.log(myModulo);

//  Opérateur d'additino et d'affectaiton : +=
var first = 10;
var second = 5;

//  first = first + Second;
first += second;
console.log(first);

// Opérateur de soustraction et d'affectation -=
second -= first;
console.log(second);